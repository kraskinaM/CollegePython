print("| Средняя скорость        | v              | 500                  | м/с      |")
print("| Масса одной молекулы    | m₀             | 4.65e-26             | кг       |")
print("| Площадь стенки          | S              | 1                    | м²       |")
print("| Расстояние между стенками| L             | 1                    | м        |")
print("| Время наблюдения        | t              | 1                    | с        |")
print("+-------------------------+----------------+----------------------+----------+")
N = 1000
v = 500
m0 = 4.65e-26
S = 1
L = 1
t = 1
print("\n--- РЕШЕНИЕ (пошаговый расчёт) ---")
N_udarov = (1/6) * N * v / L
print(f"1) Количество ударов за 1 с:")
print(f"   N_ударов = (1/6) * N * v / L = (1/6) * {N} * {v} / {L} = {N_udarov:.2e} ударов/с")
F = N_udarov * 2 * m0 * v
print(f"\n2) Сила давления на стенку:")
print(f"   F = N_ударов * 2 * m₀ * v = {N_udarov:.2e} * 2 * {m0:.2e} * {v} = {F:.4e} Н")
p = F / S
print(f"\n3) Давление газа:")
print(f"   p = F / S = {F:.4e} / {S} = {p:.4e} Па")
V = L * S
n = N / V
p_mkt = (1/3) * n * m0 * v**2
print(f"\n4) Проверка по основному уравнению МКТ:")
print(f"   p = (1/3)·n·m₀·v² = (1/3) * ({n:.2e}) * {m0:.2e} * {v}² = {p_mkt:.4e} Па")
print(f"   Совпадение с предыдущим расчётом: {'ДА' if abs(p - p_mkt) < 1e-10 else 'НЕТ'}")
print("\n--- ОТВЕТ (таблица результатов) ---")
