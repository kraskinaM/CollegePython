M = 18
rho = 1000
NA = 6.02e23
V_drop_mm3 = 1
M_kg = M / 1000
V_drop_m3 = V_drop_mm3 * 1e-9
print("\n--- РЕШЕНИЕ (формулы и расчёт) ---")
m0 = M_kg / NA
print(f"1) Масса молекулы: m₀ = M / N_A = {M_kg} / {NA:.2e} = {m0:.4e} кг")
V0 = M_kg / (rho * NA)
print(f"2) Объём молекулы: V₀ = M / (ρ·N_A) = {M_kg} / ({rho} * {NA:.2e}) = {V0:.4e} м³")
d = V0 ** (1/3)
print(f"3) Диаметр молекулы: d = ∛(V₀) = ∛({V0:.4e}) = {d:.4e} м  ({d*1e10:.2f} Å)")
N = (rho * V_drop_m3 * NA) / M_kg
print(f"4) Число молекул в капле: N = (ρ·V_капли·N_A) / M = ({rho} * {V_drop_m3:.2e} * {NA:.2e}) / {M_kg} = {N:.2e} шт")
