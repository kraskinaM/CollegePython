import math

# ============================================================
# ЗАДАЧА 1. Пружинный маятник
# ============================================================

print("=" * 50)
print("Задача 1. Пружинный маятник")
print("=" * 50)

# ---------- ДАНО ----------
m = 0.2      # масса груза, кг
k = 50.0     # жёсткость пружины, Н/м
A = 0.05     # амплитуда колебаний, м

# ---------- РЕШЕНИЕ ----------
# 1. Период колебаний T = 2π * sqrt(m/k)
T = 2 * math.pi * math.sqrt(m / k)

# 2. Частота ν = 1 / T
nu = 1 / T

# 3. Циклическая частота ω = sqrt(k/m)
omega = math.sqrt(k / m)

# 4. Полная механическая энергия E = k * A^2 / 2
#    При гармонических колебаниях:
#    E_kin_max = E_pot_max = E
E_max = (k * A ** 2) / 2

# ---------- ВЫВОД ----------
print("\n▶ Дано:")
print(f"   m = {m} кг")
print(f"   k = {k} Н/м")
print(f"   A = {A} м")

print("\n▶ Решение:")
print(f"   1) Период T = 2π·√(m/k) = 2π·√({m}/{k})")
print(f"      T = {T:.4f} с")
print(f"   2) Частота ν = 1/T = {nu:.2f} Гц")
print(f"   3) Циклическая частота ω = √(k/m) = {omega:.2f} рад/с")
print(f"   4) Максимальная энергия: E_max = k·A²/2 = {k}·({A})²/2")
print(f"      E_max = {E_max:.4f} Дж")

print("\n▶ Ответ:")
print(f"   T = {T:.4f} с")
print(f"   ν = {nu:.2f} Гц")
print(f"   ω = {omega:.2f} рад/с")
print(f"   E_max = {E_max:.4f} Дж (кинетическая = потенциальной)")


# ============================================================
# ЗАДАЧА 2. Математический маятник
# ============================================================

print("\n\n" + "=" * 50)
print("Задача 2. Математический маятник")
print("=" * 50)

# ---------- ДАНО (два варианта) ----------
# Вариант А: найти период по длине и g
L = 1.0      # длина нити, м
g = 9.81     # ускорение свободного падения, м/с²

# Вариант Б: по измеренному периоду и длине найти g
T_measured = 2.0   # измеренный период, с
L_measured = 0.99  # измеренная длина, м

# ---------- РЕШЕНИЕ ----------
# Часть А. Период для заданного маятника
T_from_L = 2 * math.pi * math.sqrt(L / g)

# Часть Б. Ускорение g по измеренным данным
# Из формулы T = 2π√(L/g) → g = 4π²·L / T²
g_from_measure = (4 * math.pi ** 2 * L_measured) / (T_measured ** 2)

# ---------- ВЫВОД ----------
print("\n▶ Часть А. По известным L и g найти период")
print(f"   Дано: L = {L} м, g = {g} м/с²")
print(f"   Решение: T = 2π√(L/g) = 2π√({L}/{g})")
print(f"   T = {T_from_L:.4f} с")
print(f"   Ответ: T = {T_from_L:.4f} с")

print("\n▶ Часть Б. По измеренному периоду и длине найти g")
print(f"   Дано: T_изм = {T_measured} с, L_изм = {L_measured} м")
print(f"   Решение: g = 4π²·L / T² = 4π²·{L_measured} / {T_measured}²")
print(f"   g = {g_from_measure:.2f} м/с²")
print(f"   Ответ: g = {g_from_measure:.2f} м/с²")

print("\n" + "=" * 50)
print("Обе задачи решены.")
